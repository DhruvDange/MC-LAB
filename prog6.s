	AREA PROG6, CODE, READONLY
DIVIDEND RN 1
DIVISOR RN 2
QUOTIENT RN 3
REMAINDER RN 4
N RN 10
R RN 11
NDR RN 12

ENTRY
	MOV N, #6
	MOV R, #3
	SUB NDR, N, R
	MOV R5, #0x40000000
	MOV DIVIDEND, N
	BL FACT
	MOV N, DIVISOR
	MOV DIVIDEND, NDR
	BL FACT
	MOV NDR, DIVISOR
	MOV DIVIDEND, R
	BL FACT
	MOV R, DIVISOR
	MUL NDR, R, NDR
	BL DIV
	STR QUOTIENT, [R5]
G B G
FACT	MOV DIVISOR, #1
LOOP2	MUL DIVISOR, DIVIDEND, DIVISOR
		SUBS DIVIDEND, #1
		BNE LOOP2
		BX LR
DIV		MOV QUOTIENT, #0
		MOV DIVISOR, NDR
		MOV DIVIDEND, N
LOOP3	SUBS DIVIDEND, DIVIDEND, DIVISOR
		ADDPL QUOTIENT, QUOTIENT, #1
		BPL LOOP3
		ADDMI REMAINDER, DIVIDEND, DIVISOR
		BX LR
		END
		